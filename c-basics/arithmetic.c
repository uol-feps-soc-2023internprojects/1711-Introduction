#include <stdio.h>

int main()
{
    const int value = 12;
    printf("Your value + 3 = %d\n", value + 3);
    printf("Your value - 3 = %d\n", value - 3);
    printf("Your value * 3 = %d\n", value * 3);
    printf("Your value / 3 = %d\n", value / 3);
    printf("Your value modulo 3 = %d\n", value % 3);
}